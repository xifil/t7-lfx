		std::vector<std::uint8_t> file_to_byte_array(const std::string& filename) {
			std::ifstream file(filename, std::ios::binary | std::ios::ate);
			if (!file) {
				throw std::runtime_error("Error: Could not open file " + filename);
				return {};
			}

			std::streamsize size = file.tellg();
			file.seekg(0, std::ios::beg);

			std::vector<uint8_t> byte_array(size);
			if (!file.read(reinterpret_cast<char*>(byte_array.data()), size)) {
				throw std::runtime_error("Error: Could not read file " + filename);
				return {};
			}

			return byte_array;
		}
