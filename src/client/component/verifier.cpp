	struct component final : generic_component {
		void post_load() override {
			if (!utils::flags::has_flag("verify")) {
				return;
			}
			init_file_hashes();

			char current_dir[MAX_PATH];
			if (GetCurrentDirectoryA(sizeof(current_dir), current_dir) == 0) {
				throw std::runtime_error("Failed to get current directory.");
				return;
			}

			utils::progress_ui ui = { game::is_headless() };
			ui.set_title("BOIII Verifier");
			ui.show(false);

			std::vector<std::string> incorrect_files{};
			std::vector<std::string> missing_files{};

			std::size_t idx = 0;
			for (const auto& [file_path, file_hash] : file_hashes) {
				if (idx + 1 == file_hashes.size()) {
					ui.set_line(1, "Verification successful.");
				}
				else {
					ui.set_line(1, utils::string::va("Verifying files... (%zu/%zu)", idx + 1, file_hashes.size()));
				}
				ui.set_line(2, file_path);

				std::vector<std::uint8_t> file_data = file_to_byte_array(file_path);
				if (file_data.size() > 0) {
					std::string found_file_hash = utils::string::to_lower(utils::cryptography::md5::compute(file_data.data(), file_data.size(), true));
					if (found_file_hash != file_hash) {
						incorrect_files.push_back(file_path);
					}
				}
				else {
					missing_files.push_back(file_path);
				}

				ui.set_progress(idx + 1, file_hashes.size());

				idx++;
			}

			const auto display_info = [](const std::string& message) {
				MessageBoxA(nullptr, message.c_str(), "BOIII Verifier", MB_OK);
			};

			if (incorrect_files.empty() && missing_files.empty()) {
				display_info("All files check out!");
			}
			else {
				std::string msg = "Some files did not check out.";
				if (incorrect_files.size() > 0) {
					msg += "\n\nIncorrect files:\n - ";
					msg += utils::string::join(incorrect_files, "\n - ");
				}
				if (missing_files.size() > 0) {
					msg += "\n\nMissing files:\n - ";
					msg += utils::string::join(missing_files, "\n - ");
				}
				display_info(msg);
			}
		}
	};
